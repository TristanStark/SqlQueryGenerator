CREATE TABLE CUSTOMER (
    CUSTOMER_ID NUMBER(10) NOT NULL,
    NAME VARCHAR2(150) NOT NULL,
    CITY VARCHAR2(100),
    CONSTRAINT PK_CUSTOMER PRIMARY KEY (CUSTOMER_ID)
);

CREATE TABLE ORDERS (
    ORDER_ID NUMBER(10) NOT NULL,
    CUSTOMER_ID NUMBER(10) NOT NULL,
    ORDER_DATE DATE,
    STATUS VARCHAR2(20),
    AMOUNT NUMBER(12,2),
    CONSTRAINT PK_ORDERS PRIMARY KEY (ORDER_ID)
);

CREATE TABLE ORDER_LINE (
    ORDER_LINE_ID NUMBER(10) NOT NULL,
    ORDER_ID NUMBER(10) NOT NULL,
    PRODUCT_ID NUMBER(10),
    QUANTITY NUMBER(10),
    UNIT_PRICE NUMBER(12,2),
    CONSTRAINT PK_ORDER_LINE PRIMARY KEY (ORDER_LINE_ID)
);

CREATE TABLE PRODUCT (
    PRODUCT_ID NUMBER(10) NOT NULL,
    LABEL VARCHAR2(200),
    CATEGORY VARCHAR2(80),
    CONSTRAINT PK_PRODUCT PRIMARY KEY (PRODUCT_ID)
);

COMMENT ON TABLE CUSTOMER IS 'Customers known by the company';
COMMENT ON COLUMN CUSTOMER.CUSTOMER_ID IS 'Technical customer identifier';
COMMENT ON COLUMN CUSTOMER.NAME IS 'Customer display name';
COMMENT ON COLUMN ORDERS.CUSTOMER_ID IS 'Reference to customer';
COMMENT ON COLUMN ORDER_LINE.ORDER_ID IS 'Reference to order';
COMMENT ON COLUMN ORDER_LINE.PRODUCT_ID IS 'Reference to product';
