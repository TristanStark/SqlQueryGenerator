/// <summary>
/// Represents the complete output of the aggregate goal heuristic.
/// </summary>
public sealed record AggregateGoalHeuristicResult
{
    /// <summary>
    /// Gets an empty result used when no aggregate-specific purpose exists.
    /// </summary>
    public static AggregateGoalHeuristicResult Empty { get; } = new()
    {
        Kind = AggregateGoalKind.None,
        Title = "Aucun agrégat détecté",
        Summary = "La requête ne contient pas assez d'agrégats pour déterminer un but statistique.",
        Confidence = 0,
        ConfidenceLevel = GoalConfidenceLevel.Low
    };

    /// <summary>
    /// Gets the inferred aggregate goal kind.
    /// </summary>
    public AggregateGoalKind Kind { get; init; }

    /// <summary>
    /// Gets the short title shown in the UI.
    /// </summary>
    public string Title { get; init; } = string.Empty;

    /// <summary>
    /// Gets the longer natural-language explanation shown in the UI.
    /// </summary>
    public string Summary { get; init; } = string.Empty;

    /// <summary>
    /// Gets the confidence score between 0 and 1.
    /// </summary>
    public double Confidence { get; init; }

    /// <summary>
    /// Gets the confidence bucket derived from <see cref="Confidence"/>.
    /// </summary>
    public GoalConfidenceLevel ConfidenceLevel { get; init; }

    /// <summary>
    /// Gets the metrics detected in the aggregate query.
    /// </summary>
    public IReadOnlyList<AggregateMetricIntent> Metrics { get; init; } = Array.Empty<AggregateMetricIntent>();

    /// <summary>
    /// Gets the grouping dimensions detected in the aggregate query.
    /// </summary>
    public IReadOnlyList<GroupingIntent> Groupings { get; init; } = Array.Empty<GroupingIntent>();

    /// <summary>
    /// Gets the ordered reasons explaining the inference.
    /// </summary>
    public IReadOnlyList<AggregateGoalReason> Reasons { get; init; } = Array.Empty<AggregateGoalReason>();

    /// <summary>
    /// Gets non-blocking warnings to show near the goal explanation.
    /// </summary>
    public IReadOnlyList<string> Warnings { get; init; } = Array.Empty<string>();

    /// <summary>
    /// Gets a suggested user-facing phrase that can be displayed as the interpreted request.
    /// </summary>
    public string? SuggestedUserPhrase { get; init; }

    /// <summary>
    /// Gets whether the result is considered useful enough to replace a generic goal explanation.
    /// </summary>
    public bool IsUseful => Kind != AggregateGoalKind.None && Confidence >= 0.35;
}
