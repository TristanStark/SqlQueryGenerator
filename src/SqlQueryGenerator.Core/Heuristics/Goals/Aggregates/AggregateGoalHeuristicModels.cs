/// <summary>
/// Contains the minimal query state required by the aggregate goal heuristic.
/// </summary>
public sealed record AggregateQuerySnapshot
{
    /// <summary>
    /// Gets an empty immutable query snapshot.
    /// </summary>
    public static AggregateQuerySnapshot Empty { get; } = new();

    /// <summary>
    /// Gets the tables used by the query.
    /// </summary>
    public IReadOnlyList<TableUsageSummary> Tables { get; init; } = Array.Empty<TableUsageSummary>();

    /// <summary>
    /// Gets aggregate projections selected by the query.
    /// </summary>
    public IReadOnlyList<AggregateProjection> Aggregates { get; init; } = Array.Empty<AggregateProjection>();

    /// <summary>
    /// Gets grouping dimensions used by the query.
    /// </summary>
    public IReadOnlyList<GroupingProjection> Groupings { get; init; } = Array.Empty<GroupingProjection>();

    /// <summary>
    /// Gets filters applied before aggregation.
    /// </summary>
    public IReadOnlyList<FilterCondition> Filters { get; init; } = Array.Empty<FilterCondition>();

    /// <summary>
    /// Gets filters applied after aggregation.
    /// </summary>
    public IReadOnlyList<HavingCondition> HavingConditions { get; init; } = Array.Empty<HavingCondition>();

    /// <summary>
    /// Gets ordering expressions applied to the query.
    /// </summary>
    public IReadOnlyList<OrderProjection> Orderings { get; init; } = Array.Empty<OrderProjection>();

    /// <summary>
    /// Gets index coverage facts computed by the existing index analyzer.
    /// </summary>
    public IReadOnlyList<IndexCoverageSummary> IndexCoverage { get; init; } = Array.Empty<IndexCoverageSummary>();

    /// <summary>
    /// Gets the SQL limit, fetch first, or top value when the query has one.
    /// </summary>
    public int? Limit { get; init; }

    /// <summary>
    /// Gets the generated SQL text when available.
    /// </summary>
    public string? GeneratedSql { get; init; }

    /// <summary>
    /// Gets whether the query builder detected that the user explicitly wants table.*.
    /// </summary>
    public bool HasStarProjection { get; init; }
}

