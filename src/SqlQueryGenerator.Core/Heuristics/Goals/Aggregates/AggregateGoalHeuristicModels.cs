namespace SqlQueryGenerator.Core.Heuristics.Goals.Aggregates;

/// <summary>
/// Describes the aggregate function detected inside a selected expression.
/// </summary>
public enum AggregateFunction
{
    /// <summary>
    /// Indicates that no aggregate function was detected.
    /// </summary>
    None = 0,

    /// <summary>
    /// Counts rows or non-null values with COUNT.
    /// </summary>
    Count = 1,

    /// <summary>
    /// Counts unique values with COUNT DISTINCT.
    /// </summary>
    CountDistinct = 2,

    /// <summary>
    /// Sums numeric values with SUM.
    /// </summary>
    Sum = 3,

    /// <summary>
    /// Computes an average value with AVG.
    /// </summary>
    Average = 4,

    /// <summary>
    /// Computes the minimum value with MIN.
    /// </summary>
    Minimum = 5,

    /// <summary>
    /// Computes the maximum value with MAX.
    /// </summary>
    Maximum = 6,

    /// <summary>
    /// Computes a median value through a database-specific expression.
    /// </summary>
    Median = 7,

    /// <summary>
    /// Computes a standard deviation through a database-specific expression.
    /// </summary>
    StandardDeviation = 8,

    /// <summary>
    /// Computes a variance through a database-specific expression.
    /// </summary>
    Variance = 9,

    /// <summary>
    /// Represents a supported but non-standard or user-defined aggregate.
    /// </summary>
    Custom = 99
}

/// <summary>
/// Describes the main human-readable goal inferred for a query containing aggregates.
/// </summary>
public enum AggregateGoalKind
{
    /// <summary>
    /// No aggregate-specific purpose could be inferred.
    /// </summary>
    None = 0,

    /// <summary>
    /// The query mainly counts rows or entities.
    /// </summary>
    RowCount = 1,

    /// <summary>
    /// The query mainly counts unique values.
    /// </summary>
    DistinctCount = 2,

    /// <summary>
    /// The query mainly computes totals.
    /// </summary>
    MetricTotal = 3,

    /// <summary>
    /// The query mainly computes averages.
    /// </summary>
    MetricAverage = 4,

    /// <summary>
    /// The query mainly computes minimums, maximums, or ranges.
    /// </summary>
    MetricExtremes = 5,

    /// <summary>
    /// The query mainly distributes values by one or more dimensions.
    /// </summary>
    Distribution = 6,

    /// <summary>
    /// The query mainly ranks groups or entities by an aggregate metric.
    /// </summary>
    Ranking = 7,

    /// <summary>
    /// The query mainly tracks an aggregate over time.
    /// </summary>
    TimeSeries = 8,

    /// <summary>
    /// The query builds a compact dashboard-like summary with several metrics.
    /// </summary>
    DashboardSummary = 9,

    /// <summary>
    /// The query computes a filtered KPI without grouping.
    /// </summary>
    FilteredKpi = 10,

    /// <summary>
    /// The query looks like a data-quality or completeness check.
    /// </summary>
    DataQuality = 11,

    /// <summary>
    /// The query mixes several aggregate intentions without one dominant pattern.
    /// </summary>
    MixedAggregateSummary = 12
}

/// <summary>
/// Describes the confidence bucket attached to a goal result.
/// </summary>
public enum GoalConfidenceLevel
{
    /// <summary>
    /// The engine has too little signal to be confident.
    /// </summary>
    Low = 0,

    /// <summary>
    /// The engine found useful signals, but the purpose remains partly ambiguous.
    /// </summary>
    Medium = 1,

    /// <summary>
    /// The engine found strong and coherent signals.
    /// </summary>
    High = 2
}

/// <summary>
/// Represents one selected aggregate expression in the query builder.
/// </summary>
public sealed record AggregateProjection
{
    /// <summary>
    /// Initializes a new aggregate projection.
    /// </summary>
    /// <param name="expressionSql">The SQL fragment of the selected aggregate expression.</param>
    /// <param name="function">The aggregate function detected by the builder or parser.</param>
    public AggregateProjection(string expressionSql, AggregateFunction function)
    {
        ExpressionSql = expressionSql ?? string.Empty;
        Function = function;
        ArgumentColumns = Array.Empty<string>();
    }

    /// <summary>
    /// Gets the raw SQL expression selected by the user or generated by the query builder.
    /// </summary>
    public string ExpressionSql { get; init; }

    /// <summary>
    /// Gets the aggregate function represented by <see cref="ExpressionSql"/>.
    /// </summary>
    public AggregateFunction Function { get; init; }

    /// <summary>
    /// Gets the generated or user-defined alias of the aggregate column.
    /// </summary>
    public string? Alias { get; init; }

    /// <summary>
    /// Gets the source table name when the aggregate argument comes from a known table.
    /// </summary>
    public string? SourceTable { get; init; }

    /// <summary>
    /// Gets the source column name when the aggregate argument comes from a known column.
    /// </summary>
    public string? SourceColumn { get; init; }

    /// <summary>
    /// Gets the display name to use in natural-language explanations.
    /// </summary>
    public string? DisplayName { get; init; }

    /// <summary>
    /// Gets the source column comment, when the schema parser loaded comments.
    /// </summary>
    public string? SourceColumnComment { get; init; }

    /// <summary>
    /// Gets whether the aggregate expression uses DISTINCT.
    /// </summary>
    public bool IsDistinct { get; init; }

    /// <summary>
    /// Gets the list of known argument columns referenced by the aggregate expression.
    /// </summary>
    public IReadOnlyList<string> ArgumentColumns { get; init; }

    /// <summary>
    /// Gets whether this aggregate computes a numeric measure rather than a pure count.
    /// </summary>
    public bool IsMeasureAggregate => Function is AggregateFunction.Sum or AggregateFunction.Average or AggregateFunction.Minimum or AggregateFunction.Maximum or AggregateFunction.Median or AggregateFunction.StandardDeviation or AggregateFunction.Variance;
}

